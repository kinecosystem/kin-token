# Helper funcs.

# Test if testrpc is running on port $1. 
# Result is in $?
testrpc_running() {
  nc -z localhost $1
}

# Kills testrpc process with its PID in $testrpc_pid.
cleanup() {
  echo "cleaning up"
  # Kill the testrpc instance that we started (if we started one).
  if [ -n "$testrpc_pid" ]; then
    kill -9 $testrpc_pid
  fi
}
